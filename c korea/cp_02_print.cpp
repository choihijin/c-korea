#include <stdio.h>

void main2() {
	//자주 사용하는 데이터는 저장해놓자 변수
	char name = 'f';   //변수
	int number = 123;
	float number2 = 3.14f;
	//변수 :저장공간
	//저장 공간의 저장공간의형태 
	
	
	
	printf("hello%d\n",number);  // 숫자출력 
	printf("hello%d\n",number);	//숫자출력
	printf("hello%c\n",name);	//글자출력
	printf("hello%f\n",number2);
	// %c 한글자
	// %d 숫자 
	// %f 소수점 
	// %s 한글자 
}



